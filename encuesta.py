def entrada():
    names = []
    ideas = []
    
    for i in range(10):
        print(f"\nCompañero {i+1}:")
        nombre = input("Ingresa tu nombre: ")
        idea = input("Ingresa tu idea de proyecto: ")

        names.append(nombre)
        ideas.append(idea)
    return names, ideas  

def resultado(names, ideas): 
    print("===========================================")
    print("--------RESULTADO DE LA ENCUESTA-----------")
    print("===========================================")
    for j in range(len(names)):
        print(f"{j+1}. {names[j]} tiene una idea de proyecto de: {ideas[j]}")

